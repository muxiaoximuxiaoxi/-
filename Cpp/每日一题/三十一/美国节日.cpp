        int dd=0;
        if(ww==1)
            dd=31-6;
        else  
            dd=31-(ww-2);
        //int dd=31-((ww==1)?6:(ww-2));
        printf("%d-05-%02d\n",year,dd);//阵亡将士纪念日
        //* 5月的最后一个星期一：阵亡将士纪念日
        printf("%d-07-04\n",year);//美国国庆
